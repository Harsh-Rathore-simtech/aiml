                <script>
                    document.addEventListener('DOMContentLoaded', function() {
                        const cards = document.querySelectorAll('.package-card');
                        const input = document.getElementById('squareFeet');
                        const display = document.getElementById('estimatedPrice');
                        
                        let currentRate = 500;

                        function updatePrice() {
                            const sqft = parseFloat(input.value) || 0;
                            const total = sqft * currentRate;
                            
                            // Format to Indian Rupees
                            display.textContent = '₹' + total.toLocaleString('en-IN');
                        }

                        cards.forEach(card => {
                            card.addEventListener('click', function() {
                                // Update active class
                                cards.forEach(c => c.classList.remove('active'));
                                this.classList.add('active');
                                
                                // Update rate
                                currentRate = parseInt(this.dataset.rate);
                                
                                updatePrice();
                            });
                        });

                        input.addEventListener('input', updatePrice);
                    });
